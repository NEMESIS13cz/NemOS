void font_draw_char(char character, long x, long y, long color) {
	if (character == '\n') {
		return;
	}
	gfx_draw_bitmap_transparent(font_get_bitmap(character), x, y, 7, 14, color);
}

void font_draw_string(char* string, long x, long y, long color) {
	long current_x = x;
	long l = 0;
	while (1) {
		if (string[l] == '\n') {
			current_x = x;
			y += 16;
		}else if (string[l] == '\0') {
			return;
		}else{
			gfx_draw_bitmap_transparent(font_get_bitmap(string[l]), current_x, y, 7, 14, color);
			current_x += 8;
		}
		l++;
	}
}
